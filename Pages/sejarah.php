            <h1>Mengenal Sejarah</h1>
            <h2>SMP Negeri 5 Malang</h2>
            <p>SMP Negeri 5 Malang yang berdiri pada tahun 1960 memiliki sejarah cukup panjang di dunia pendidikan Kota Malang. Tak banyak yang mengetahui jika SMP ini dulu merupakan Sekolah Guru Bawah (SGB) yang dibangun pada masa kemerdekaan.</p>
            <p>Pada saat awal kemerdekaan Republik Indonesia, Kota Malang ditunjuk sebagai salah satu kota yang menyelenggarakan SGB. Sekolah ini merupakan tempat pendidikan bagi para calon guru Sekolah Rakyat (SR). Pada masa itu, setelah lulus dari SR, orang bisa menjadi seorang pengajar. Syaratnya harus menyelesaikan dulu pendidikan di SR.</p>
            <p>Lama pendidikan SGB adalah empat tahun. Pada tahun pertama hingga ketiga, para calon guru menerima materi pelajaran umum yang menjadi prioritas. Kemudian, pada akhir tahun ketiga diadakan ujian. Jika mendapatkan nilai baik, calon guru dapat meneruskan ke Sekolah Guru Atas (SGA). Selanjutnya, pada tahun keempat, para calon guru lebih banyak mendapatkan materi pendidikan didaktik, metodik, ilmu jiwa dan praktik mengajar.</p>
            <p>Pada tahun 1960, pemerintah Indonesia menghapuskan SGB. Karena alasan tersebut, SGB 2 Malang kemudian diubah menjadi SMP Negeri 5 Malang. Keberadaannya diresmikan melalui Surat Keputusan Menteri Pendidikan, Pengajaran dan Kebudayaan Republik Indonesia, dengan nomor 186/Perw/SMP/SK/'60. Sekolah ini memiliki Nomor Pokok Sekolah Nasional (NPSN) 20537367, dan Nomor Statistik Sekolah (NSS) 20.1.05.61.01.004.</p>
            <p>Pada tahun 2007, Departemen Pendidikan Nasional (Depdiknas) menetapkan sejumlah SMP di Indonesia dengan status Rintisan Sekolah Bertaraf Internasional (RSBI). SMP Negeri 5 Malang mendapatkan status tersebut pada tahun pelajaran 2008-2009, melalui Surat Keputusan Direktur PSMP Dirjen Mandikdasmen No. 230/C3/Kep/2008.</p>
